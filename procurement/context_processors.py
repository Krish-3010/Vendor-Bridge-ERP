def app_context(request):
    if not request.user.is_authenticated:
        return {}
    role = getattr(getattr(request.user, "profile", None), "role", "")
    nav_routes = [
        ("dashboard",       "Dashboard"),
        ("vendors",         "Vendors"),
        ("rfqs",            "RFQs"),
        ("quotations",      "Quotations"),
        ("approvals",       "Approvals"),
        ("purchase_orders", "Purchase Orders"),
        ("invoices",        "Invoices"),
        ("reports",         "Reports"),
        ("activity",        "Activity"),
    ]
    nav_items = [
        (route, label, NAV_ICONS.get(route, ""))
        for route, label in nav_routes
    ]

    # Live badge counts for sidebar alerts (non-vendors only)
    quoted_rfq_count = 0
    pending_approvals_count = 0
    if role != Profile.VENDOR:
        quoted_rfq_count = RFQ.objects.filter(status=RFQ.QUOTED).count()
        pending_approvals_count = Approval.objects.filter(status=Approval.PENDING).count()

    return {
        "current_role":           role,
        "unread_count":           Notification.objects.filter(user=request.user, is_read=False).count(),
        "nav_items":              nav_items,
        "quoted_rfq_count":       quoted_rfq_count,
        "pending_approvals_count": pending_approvals_count,
    }
